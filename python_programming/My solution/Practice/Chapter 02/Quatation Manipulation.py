# Quatation Manipulation
# Demostrates string methods

# quate from IBM Chairman, Thomas Watson, in 1943
quate = "I think there is a World market for maybe five computers."

print("Original quate")
print(quate)

print("\n\nuppercase")
print(quate.upper())

print("\n\nowercase")
print(quate.lower())

print("\n\nsentence case")
print(quate.title())

print("\n\nWith minor replacement")
print(quate.replace("five","millions of"))

print("\n\nOriginal quate is still:")
print(quate)

input("\n\nPress enter key to exit")
